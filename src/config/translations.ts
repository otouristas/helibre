export const getLangFromPath = (pathname: string): Locale => {
  if (pathname.startsWith('/nl')) return 'nl';
  if (pathname.startsWith('/fr')) return 'fr';
  if (pathname.startsWith('/el')) return 'el';
  return 'en';
};
